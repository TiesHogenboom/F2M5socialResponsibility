gevoel niet goed genoeg te zijn. Ik sprak hier nooit met
iemand over, uit angst dat anderen mij raar of zwak zouden
vinden. Ik zette een masker op en probeerde in mijn eentje
mijn gevoelens op te lossen. Ik heb hier een hoge prijs voor
betaald: op mijn 23e belandde ik in een depressie.

Ik weet dat mijn ervaring niet uniek is. We worstelen allemaal
met de vraag 'Ben ik goed genoeg? Doe ik er toe?'
Het probleem is dat we er niet over praten en niet weten
wat kunnen doen om onze mentale gezondheid te verbeteren.
Dat terwijl onze mentale gezondheid steeds meer onder druk
komt te staan, de wachtlijsten van de GGZ alsmaar langer
worden, en de nadruk nog steeds ligt op genezen in plaats
van voorkomen.

Vanuit deze frustratie richtte ik de Transformers Community op:
een plek waar het oke is om niet oke te zijn!
